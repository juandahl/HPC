// -----------------------------------------------------------------
void mul_f32matrix_ijk(float32 **A, float32 **B, float32 **C, int n)
// -----------------------------------------------------------------
{
	int i, j, k, res;
	for(i = 0; i < n; i++){
		for(j = 0; j < n; j++){
			res = 0;
			for(k = 0; k < n; k++){
				res += A[i][k]*B[k][j];
			}
			C[i][j] = res; 
		}
	} 
}
